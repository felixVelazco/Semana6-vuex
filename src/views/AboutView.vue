<h2 class="letraMorada">¿Historia?</h2><br>
<p>Estos pasteles se tomaban solo en ocasiones especiales. 
    Su textura esponjosa y delicado relleno elaborado con pasta de azuki de la mejor calidad, 
    estaba solo destinado a aquellas personas que podían permitirse pagar el elevado precio de esta exquisitez.
    Debido a su delicioso sabor, y al tamaño de la ración perfecta para llevar, este pastel se extendió rápidamente por todo el país. Al principio solo para tomar en días especiales, para después pasar a ser un dulce que se encuentra en todas partes, ya que a los jóvenes japoneses, y sobre todo a los turistas, llama muchísimo la atención.
</p>

