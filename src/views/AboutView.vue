<h2 class="letraMorada">¿Qué es nekoyaki?</h2>
<p>El taiyaki es un pastel japonés que tiene forma de pez y tradicionalmente está relleno con pasta de judías rojas azuki, aunque a día de hoy puedes encontrarlo de infinitos rellenos distintos. 
